GdkPixbuf * Load_Icon_TGTK()  {
	
   
   GdkPixbuf *pixbuf;
   GError * err = NULL;
   
   if( pl == NULL ){
   
      pl = gdk_pixbuf_loader_new_with_type( "png", NULL );
      
      gdk_pixbuf_loader_write( pl, ( guchar *) pf_tgtk, sizeof( pf_tgtk ), &err );  
   
      if( err != NULL )
      {
      	   g_print( "error loading tgtk default icon" );
      	   return ( GdkPixbuf * ) 0;
      }
   
      gdk_pixbuf_loader_close(pl, NULL);
      
      pixbuf = gdk_pixbuf_loader_get_pixbuf( pl );
   
      if (GDK_IS_PIXBUF(pixbuf))
          return pixbuf;	
   	
	}
	
	 return ( GdkPixbuf * ) 0;
	
}

void Free_TGTK_Icon(){
	g_object_unref(pl);
	pl = NULL;
}
	
