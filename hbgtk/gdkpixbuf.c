HB_FUNC( GDK_PIXBUF_NEW_FROM_FILE ) // cFilename -> pixbuf
{
  GdkPixbuf * pixbuf;
  GError    * error = NULL;
  gboolean has_alpha = TRUE ;  // transparent
  int bits_per_sample = 8;
  int width = 1;
  int height = 1;

  pixbuf = gdk_pixbuf_new_from_file( (const gchar *) hb_parc( 1 ), &error );
  if (!pixbuf)
  {
     g_print( "Error de apertura : %s", error->message );
     g_error_free (error);
     pixbuf = gdk_pixbuf_new( GDK_COLORSPACE_RGB, has_alpha, 
                              bits_per_sample, width, height);
  }
  hb_retptr( (GdkPixbuf *) pixbuf );
}

//----------------------------------------------//

HB_FUNC( GDK_PIXBUF_NEW_FROM_BUFFER ) // cType, Buffer -> pixbuf
{
   
   gchar * cType = ( gchar * ) hb_parc( 1 );
   guchar * cBuffer = ( guchar * ) hb_parc( 2 );
   GError * err = NULL;
   GdkPixbufLoader * pLoad;
   GdkPixbuf * pPixBuf;
   
   pLoad = gdk_pixbuf_loader_new_with_type( cType, NULL );
   
   gdk_pixbuf_loader_write( pLoad, cBuffer, hb_parclen( 2 ), &err );
   
   gdk_pixbuf_loader_close( pLoad, NULL );

   pPixBuf = gdk_pixbuf_loader_get_pixbuf( pLoad );
   
   hb_retptr( (GdkPixbuf *) pPixBuf );

}

